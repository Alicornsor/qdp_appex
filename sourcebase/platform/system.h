/////////////////////////////////////////////////////////////////////////
///CByte表示一个单字节的网络传输数据对象
///@author	xuzh
///@version	1.0,20020214
///	任何表示网络报文的数据结构中，如果要表示单字节的整数，必须使用本类
/////////////////////////////////////////////////////////////////////////
class CByte
{
private:
	///表示这个单字节整数
	unsigned char value;
public:
	///设置本对象的值
	///@param	v	设置的值
	void setValue(int v);
	
	///获取本对象的值
	///@return	本对象的当前值
	int getValue(void);
};

